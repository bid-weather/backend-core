CREATE TABLE prediction_daily (
  id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  date                DATE            NOT NULL,
  category_id         BIGINT          NOT NULL,
  subcategory_id      BIGINT          NOT NULL,
  count               INTEGER         NOT NULL,
  predicted_at        DATE            NOT NULL,

  CONSTRAINT fk_prediction_date FOREIGN KEY (date) REFERENCES calendar_date(date) ON DELETE CASCADE,
  CONSTRAINT fk_prediction_category FOREIGN KEY (category_id) REFERENCES category(id),
  CONSTRAINT fk_prediction_subcategory FOREIGN KEY (subcategory_id) REFERENCES subcategory(id)
);